# developer_type.py

def determine_type_developer(langage):
    if langage == "JavaScript":
        print("Tu es un développeur web")
    elif langage == "python":
        print("Tu es un développeur IA")
    elif langage == "java":
        print("Tu es un développeur logiciel")
    elif langage == "reactjs":
        print("Tu es un développeur mobile")
    else:
        print("Un jour, je serai le meilleur développeur...")

# Appel de la fonction avec différents paramètres
determine_type_developer("JavaScript")
determine_type_developer("python")
determine_type_developer("java")
determine_type_developer("reactjs")
determine_type_developer("C#")
