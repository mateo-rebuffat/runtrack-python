# inverse_string.py

def inverse_string(chaine):
    return chaine[::-1]

# Test de la fonction avec différentes valeurs
resultat1 = inverse_string("nikana")
resultat2 = inverse_string("Hello World!")
resultat3 = inverse_string("python")

# Affichage des résultats
print(resultat1)
print(resultat2)
print(resultat3)
